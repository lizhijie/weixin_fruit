package cn.data.table;

public class Goods {
private int id;
private String alias;
private String name;
private String about;
private int price;
private String time;
private String desxml;
private String groups;
private int status;


public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getAlias() {
	return alias;
}
public void setAlias(String alias) {
	this.alias = alias;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getAbout() {
	return about;
}
public void setAbout(String about) {
	this.about = about;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}
public String getTime() {
	return time;
}
public void setTime(String time) {
	this.time = time;
}
public String getDesxml() {
	return desxml;
}
public void setDesxml(String desxml) {
	this.desxml = desxml;
}
public String getGroups() {
	return groups;
}
public void setGroups(String group) {
	this.groups = group;
}
public int getStatus() {
	return status;
}
public void setStatus(int status) {
	this.status = status;
}




}
