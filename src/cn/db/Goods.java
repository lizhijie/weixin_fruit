package cn.db;

public class Goods {
public int id;
public String name;
public String imgurl;
public String about;
public int price;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getPrice() {
	return price;
}
public void setPrice(int price) {
	this.price = price;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getImgurl() {
	return imgurl;
}
public void setImgurl(String imgurl) {
	this.imgurl = imgurl;
}
public String getAbout() {
	return about;
}
public void setAbout(String about) {
	this.about = about;
}


}
